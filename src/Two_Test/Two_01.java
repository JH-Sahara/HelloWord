package Two_Test;
// /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// 编写程序显示 100 到 1000 之间能被 5 和 6 整除的数
// /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

public class Two_01 {
    public static void main(String args[ ]){
        int i = 100;
        for( ; i<=1000; i++)
        {
            if(i%5==0 && i%6==0)
                System.out.print(i+"\t");
        }
    }
}
